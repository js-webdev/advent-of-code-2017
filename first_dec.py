def captcha_one(string):
    """captcha one"""
    array = list(string)
    counter = 0
    mysum = 0
    for i, value in enumerate(array):
        if value == array[i-1]:
            mysum += int(value)
        if counter < 1:
            if i == len(array):
                counter += 1
                i = 0
        else:
            if i == 1:
                break
    print(mysum)


def captcha_two(string):
    """captcha two"""
    array = list(string)
    arrlen = len(array)
    halfway = int(arrlen/2)
    counter = 0
    mysum = 0
    for i, value in enumerate(array):
        if (i+halfway) >= arrlen:
            helper = i+halfway - arrlen
            if value == array[helper]:
                mysum += int(value)
        else:
            if value == array[i+halfway]:
                mysum += int(value)
        if counter < 1:
            if i == len(array):
                counter += 1
                i = 0
        else:
            if i == 1:
                break
    print(mysum)


